// 题目：用*号输出字母C的图案。
// 程序分析：可先用'*'号在纸上写出字母C，再分行输出。

#include <stdio.h>

int main(int argc, char *argv[])
{
    printf("000000**00\n");
    printf("0000**0000\n");
    printf("00**000000\n");
    printf("**00000000\n");
    printf("**00000000\n");
    printf("00**000000\n");
    printf("0000**0000\n");
    printf("000000**00\n");

    return 0;
}
